package model;

public class Coche {

    private int chassis;
    private int motor;
    private int aleD;
    private int aleT;
    private int fondo;
    private int pontones;
    private int refrigeracion;
    private int caja;
    private int frenos;
    private int suspension;
    private int electrico;

    public int getChassis() {
        return chassis;
    }

    public void setChassis(final int chassis) {
        this.chassis = chassis;
    }

    public int getMotor() {
        return motor;
    }

    public void setMotor(final int motor) {
        this.motor = motor;
    }

    public int getAleD() {
        return aleD;
    }

    public void setAleD(final int aleD) {
        this.aleD = aleD;
    }

    public int getAleT() {
        return aleT;
    }

    public void setAleT(final int aleT) {
        this.aleT = aleT;
    }

    public int getFondo() {
        return fondo;
    }

    public void setFondo(final int fondo) {
        this.fondo = fondo;
    }

    public int getRefrigeracion() {
        return refrigeracion;
    }

    public void setRefrigeracion(final int refrigeracion) {
        this.refrigeracion = refrigeracion;
    }

    public int getCaja() {
        return caja;
    }

    public void setCaja(final int caja) {
        this.caja = caja;
    }

    public int getFrenos() {
        return frenos;
    }

    public void setFrenos(final int frenos) {
        this.frenos = frenos;
    }

    public int getSuspension() {
        return suspension;
    }

    public void setSuspension(final int suspension) {
        this.suspension = suspension;
    }

    public int getElectrico() {
        return electrico;
    }

    public void setElectrico(final int electrico) {
        this.electrico = electrico;
    }


    public int getPontones() {
        return pontones;
    }

    public void setPontones(final int pontones) {
        this.pontones = pontones;
    }

}
